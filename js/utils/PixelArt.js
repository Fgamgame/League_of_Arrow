// Draw pixel sprite
export function drawPixelSprite(ctx, sprite, palette, x, y, scale = 2, flip = false) {
    const { width, height, pixels, colorMap } = sprite;
    const pixelSize = scale;

    ctx.save();

    if (flip) {
        ctx.translate(x, y);
        ctx.scale(-1, 1);
        ctx.translate(-x, -y);
    }

    const startX = x - (width * pixelSize) / 2;
    const startY = y - (height * pixelSize) / 2;

    for (let row = 0; row < height; row++) {
        for (let col = 0; col < width; col++) {
            const colorIndex = pixels[row][col];
            if (colorIndex === 0) continue;

            const colorKey = colorMap[colorIndex];
            const color = palette[colorKey];

            if (color) {
                ctx.fillStyle = color;
                ctx.fillRect(
                    startX + col * pixelSize,
                    startY + row * pixelSize,
                    pixelSize,
                    pixelSize
                );
            }
        }
    }

    ctx.restore();
}

// Draw player with walking animation
export function drawPlayerSprite(ctx, player, palette, x, y, scale = 2) {
    const sprite = PlayerSprite;
    const { width, height, frames, colorMap } = sprite;
    const pixelSize = scale;

    // Select frame based on walk animation
    const frameNames = ['stand', 'walk1', 'walk2', 'walk3'];
    const frameIndex = player.walkFrame % 4;
    const pixels = frames[frameNames[frameIndex]] || frames.stand;

    ctx.save();

    // Flip based on facing direction
    if (!player.facingRight) {
        ctx.translate(x, y);
        ctx.scale(-1, 1);
        ctx.translate(-x, -y);
    }

    const startX = x - (width * pixelSize) / 2;
    const startY = y - (height * pixelSize) / 2;

    for (let row = 0; row < height; row++) {
        for (let col = 0; col < width; col++) {
            const colorIndex = pixels[row][col];
            if (colorIndex === 0) continue;

            const colorKey = colorMap[colorIndex];
            const color = palette[colorKey];

            if (color) {
                ctx.fillStyle = color;
                ctx.fillRect(
                    startX + col * pixelSize,
                    startY + row * pixelSize,
                    pixelSize,
                    pixelSize
                );
            }
        }
    }

    ctx.restore();
}
